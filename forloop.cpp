// for_loop_examples.cpp
// 教學示範：單 for 迴圈三種用途
// 編譯：g++ -std=c++17 -Wall for_loop_examples.cpp -o for_demo
// 執行：./for_demo

#include <iostream>
using namespace std;

int main() {
    // ============================================================
    // 範例 1: 輸出數字 1 到 10
    // ------------------------------------------------------------
    cout << "範例 1: 輸出 1 到 10" << endl;
    for (int i = 1; i <= 10; i++) {
        cout << i << " ";
    }
    cout << "\n\n"; // 換行 + 空行

    // ============================================================
    // 範例 2: 計算 1 到 10 的總和
    // ------------------------------------------------------------
    cout << "範例 2: 計算 1 到 10 的總和" << endl;
    int sum = 0;
    for (int i = 1; i <= 10; i++) {
        sum += i;  // 每次把 i 累加到 sum
    }
    cout << "總和 = " << sum << "\n\n";

    // ============================================================
    // 範例 3: 輸出 2 到 10 的偶數
    // ------------------------------------------------------------
    cout << "範例 3: 輸出偶數 (2 ~ 10)" << endl;
    for (int i = 2; i <= 10; i += 2) {
        cout << i << " ";
    }
    cout << "\n";

    return 0;
}